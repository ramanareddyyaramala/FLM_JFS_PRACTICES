package com.task2;

public class ArithmeticOperations {
		
	
		public static void main(String[] args) {
			int a=32;
			int b=21;
			
			System.out.println(a+" + "+b +" -> "+  (a+b));
			
			System.out.println("---------------------------");
			
			int d=10;
			float e=5.5f;
			
			System.out.println(d+ " * "+e +" ->"+  (d*e));
		}
}
